#[derive(Debug, PartialEq, Clone)]
pub enum Token {
    Identifier(String),
    ConstantInteger(u64),
    ConstantFloat(f64),
    ConstantCharacter(u8),
    StringLiteral(String),
    Auto,
    Break,
    Case,
    Char,
    Const,
    Continue,
    Default,
    Do,
    Double,
    Else,
    Enum,
    Extern,
    Float,
    For,
    Goto,
    If,
    Int,
    Long,
    Register,
    Return,
    Short,
    Signed,
    Sizeof,
    Static,
    Struct,
    Switch,
    Typedef,
    Union,
    Unsigned,
    Void,
    Volatile,
    While,
    Ellipsis, // ...
    RightAssign,
    LeftAssign,
    AddAssign,
    SubAssign,
    MulAssign,
    DivAssign,
    ModAssign,
    AndAssign,
    XorAssign,
    OrAssign,
    RightOp,
    LeftOp,
    IncOp,
    DecOp,
    PtrOp,
    AndOp,
    OrOp,
    LeOp,
    GeOp,
    EqOp,
    NeOp,
    SemiColon,
    LeftBrace,
    RightBrace,
    Comma,
    Colon,
    Equal,
    LeftParen,
    RightParen,
    LeftBracket,
    RightBracket,
    Dot,
    Ampersand,
    Exclamation,
    Tilde,
    Minus,
    Plus,
    Star,
    Slash,
    Percent,
    LessThan,
    GreaterThan,
    Caret,
    Pipe,
    Question,
}
use rusty_parser as rp;

pub type DynParser = rp::DynBoxChars<(Token,)>;
